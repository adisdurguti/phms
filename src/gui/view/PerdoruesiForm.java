    private void tabelaSelectedIndexChange() {
        final ListSelectionModel rowSM = userTable.getSelectionModel();
        rowSM.addListSelectionListener(new ListSelectionListener() {
            public void valueChanged(ListSelectionEvent Ise) {
                if (Ise.getValueIsAdjusting()) {
                    return;
                }
                ListSelectionModel rowSM = (ListSelectionModel) Ise.getSource();
                int selectedIndex = rowSM.getAnchorSelectionIndex();
                if (selectedIndex > -1) {
                    Perdoruesi u = ptm.getPerdoruesi(selectedIndex);
                    userIDtxt.setText(u.getPerdoruesiID() + "");
                    //roleIDtxt.setText(u.getRoleID() + "");
                    roleTypeComboBox.setSelectedItem(u.getRoleID());
                    roleTypeComboBox.repaint();
                    userNametxt.setText(u.getUserName() + "");
                    passwordTxt.setText(u.getPassword() + "");
                    nameTxt.setText(u.getName() + "");
                    lastNameTxt.setText(u.getLastName() + "");
                    emailTxt.setText(u.getEmail() + "");
                    birthdayDateChooser.setDate(u.getBirthDate());
                    if (u.getGender() == 'M') {
                        maleRadioButton.setSelected(true);
                    } else if (u.getGender() == 'F') {
                        femaleRadioButton.setSelected(true);
                    }
                    salaryTxt.setText(u.getSalary() + "");

                }
            }
        });
    }

    public void loadTable() {
        try {
            List<Perdoruesi> list = pi.findAll();
            ptm.addList(list);
            userTable.setModel(ptm);
            ptm.fireTableDataChanged();
        } catch (PharmacyException ex) {
            Logger.getLogger(PerdoruesiForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    public void loadComboBoxCategory() {
        try {
            List<Role> list = ri.findAll();
            rtbm = new RoleTypeComboBoxModel(list);
            roleTypeComboBox.setModel(rtbm);
            roleTypeComboBox.repaint();
        } catch (PharmacyException ce) {
            JOptionPane.showMessageDialog(this, ce.getMessage());
        }
    }

