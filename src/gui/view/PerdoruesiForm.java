    boolean check() {
        if (!(roleTypeComboBox.getSelectedIndex() == -1 || userNametxt.getText().trim().equals("") || passwordTxt.getText().trim().equals("")
                || nameTxt.getText().trim().equals("") || lastNameTxt.getText().trim().equals("") || emailTxt.getText().trim().equals("") || birthdayDateChooser.getDate().equals(null) || bg.getSelection() == null
                || salaryTxt.getText().trim().equals(""))) {
            return true;
        }
        return false;
    }
     public static boolean isValid(String email) 
    { 
        String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\."+ 
                            "[a-zA-Z0-9_+&*-]+)*@" + 
                            "(?:[a-zA-Z0-9-]+\\.)+[a-z" + 
                            "A-Z]{2,7}$"; 
                              
        Pattern pat = Pattern.compile(emailRegex); 
        if (email == null) 
            return false; 
        return pat.matcher(email).matches(); 
    } 
    private void addUserBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addUserBtnActionPerformed
        // TODO add your handling code here:
        Date d = new Date();
        SimpleDateFormat s = new SimpleDateFormat("dd-MM-yyyy");
        s.format(d);
        Calendar cal = Calendar.getInstance();
        cal.add(Calendar.YEAR, -25);
        Date d1 = cal.getTime();
        Calendar cal1 = Calendar.getInstance();
        cal1.add(Calendar.YEAR, -65);
        Date dl1 = cal1.getTime();
        Perdoruesi u = new Perdoruesi();
        if (check()) {
            if (!(birthdayDateChooser.getDate().after(d))) {
                if ((birthdayDateChooser.getDate().after(dl1)) && (birthdayDateChooser.getDate().before(d1))) {
                    if (isValid(emailTxt.getText())) {
                        Perdoruesi p = new Perdoruesi();
                        try {
                            // p.setPerdoruesiID(Integer.parseInt(userIDtxt.getText()));
                            p.setRoleID((Role) roleTypeComboBox.getSelectedItem());
                            p.setUserName(userNametxt.getText());
                            p.setPassword(passwordTxt.getText());
                            p.setName(nameTxt.getText());
                            p.setLastName(lastNameTxt.getText());
                            p.setEmail(emailTxt.getText());
                            p.setBirthDate(birthdayDateChooser.getDate());
                            if (maleRadioButton.isSelected()) {
                                p.setGender('M');
                            } else if (femaleRadioButton.isSelected()) {
                                p.setGender('F');
                            }
                            BigDecimal salary = new BigDecimal(salaryTxt.getText());
                            p.setSalary(salary);

                            pi.create(p);
                            clear();
                        } catch (PharmacyException ex) {
                            Logger.getLogger(PerdoruesiForm.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    } else {
                         JOptionPane.showMessageDialog(this, "Invalid Email Address !");
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Age should be over 25 and under 65 !");
                }
            } else {
                JOptionPane.showMessageDialog(this, "BirthDate cannot be greater than the actual date!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please ,fill all the required information !");
        }
        loadTable();

    }//GEN-LAST:event_addUserBtnActionPerformed

